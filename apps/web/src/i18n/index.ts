type WidenMessages<T> = T extends string
  ? string
  : T extends readonly (infer Item)[]
    ? readonly WidenMessages<Item>[]
    : T extends object
      ? { readonly [K in keyof T]: WidenMessages<T[K]> }
      : T

type LocaleMessageSchema = WidenMessages<typeof baseMessages.en>

type DeepPartial<T> = T extends readonly (infer Item)[]
  ? readonly WidenMessages<Item>[]
  : T extends object
    ? { readonly [K in keyof T]?: DeepPartial<T[K]> }
    : T

const isRecord = (value: unknown): value is Record<string, unknown> => typeof value === 'object' && value !== null && !Array.isArray(value)

const mergeMessages = <T extends Record<string, unknown>>(base: T, overrides: DeepPartial<T>): T => {
  const result: Record<string, unknown> = { ...base }
  for (const [key, value] of Object.entries(overrides as Record<string, unknown>)) {
    const baseValue = result[key]
    result[key] = isRecord(baseValue) && isRecord(value) ? mergeMessages(baseValue, value) : value
  }
  return result as T
}

